def add(num1, num2):
    print(f"{int(num1) + int(num2)}")


def subtract(num1, num2):
    print(f"{int(num1) - int(num2)}")


def multiply(num1, num2):
    print(f"{int(num1) * int(num2)}")


def divide(num1, num2):
    print(f"{int(num1) // int(num2)}")


def main():
    add(1, 2)
    multiply(2, 4)
    subtract(5, 2)
    divide(100, 33)


main()
